<h2 class="font-weight-bold" style="color:#616a7ad5;">HISTORIA</h2>
<br>
<p style="font-size: 15px;color:black">
    El primer antecedente de la Agenda Ambiental es la Comisión de Medio Ambiente
    de la Universidad Autónoma de San Luis Potosí que nace en 1992 por iniciativa
    de profesores de las Facultades de Ingeniería, Ciencias Químicas y Medicina,
    bajo la rectoría de Lic. Alfonso Lastras Ramírez, bajo la coordinación del Dr.
    Pedro Medellín Milán. Más tarde, en 1996 se crea el Diplomado en Gestión Ambiental
    y Ecología, que lleva la multidisciplina hacia una propuesta formal de los estudios ambientales.
    <br>
    <br>
    En marzo de 1998, el Ing. Jaime Valle Méndez, rector de la universidad, instruye la construcción
    de una Coordinación de Programas Ambientales a la Comisión de Medio Ambiente. Meses después, se
    le presenta la propuesta “Hacia la construcción de una Agenda Ambiental para la Universidad Autónoma
    de San Luis Potosí”, preparada por dicha comisión y presentada en la Reunión Nacional de Programas
    Ambientales Extracurriculares en la Educación Superior, que se llevó a cabo del 24 al 26 de agosto
    en Colima. Dicho documento analiza las capacidades institucionales, académicas, administrativas y
    el potencial para crear esta oficina.
    <br>
    <br>

    Aquí se definen los primeros objetivos: integrar diversas esferas del quehacer universitario; articular
    programas ambientales entre facultades y entidades; formalizar el apoyo institucional y mecanismos de
    colaboración; generar impacto y trascendencia de los programas; y afrontar el desafío de la interdisciplina
    para la construcción social del conocimiento (Nieto Caraveo, 1998). Esta propuesta fortalece lo que ya estaba
    planteado en el Plan Institucional de Desarrollo 1997-2000 de nuestra universidad, para integrar la temática
    ambiental como eje de desarrollo y planeación universitaria.
    <br>
    <br>
    De esta forma, en 1998 se formaliza la Agenda Ambiental como una oficina dependiente de la rectoría para coordinar
    diversos aspectos en temas de ambiente y sostenibilidad de nuestra universidad. Algunos documentos declaran que:
    “La Agenda Ambiental es una iniciativa universitaria con carácter transversal y horizontal cuya misión es promover
    y apoyar la incorporación de la perspectiva ambiental y de la sostenibilidad en todo el quehacer de la Universidad,
    de manera que tenga un profundo impacto en el interior como en el exterior de la institución” (Medellín Milán,
    2004).
    <br>
    <br>
    En este mismo documento se detallan los diversos objetivos, entre los cuales destacan: incorporar la perspectiva
    ambiental
    a los currículos de licenciatura y posgrado; formar y actualizar profesores e investigadores en tópicos ambientales;
    fortalecer
    la investigación; diversificar servicios de laboratorio y consultoría en temas ambientales; mejorar el desempeño
    ambiental de
    la universidad; y desarrollar estrategias de comunicación.
    <br>
    <br>
    Al paso de los años y con la aportación de la generación del conocimiento que aporta la investigación derivada del
    Programa
    Multidiscipinario de Posgrado en Ciencias Ambientales (a partir del 2002) y otros posgrados, y la construcción de
    una cultura
    alrededor de la sostenibilidad a través de diversas estrategias, es como en el año 2019, ahora bajo la dirección del
    Dr. Marcos
    Algara Siller, se realiza un análisis general de los éxitos y áreas de oportunidad de la Agenda Ambiental. Bajo un
    trabajo participativo
    de los más de 20 colaboradores de la entidad, y las experiencias ya comentadas, la acción transversal de la Agenda
    Ambiental se estructura
    en 4 ejes: 1) Gestión institucional, 2) Educación e investigación, 3) Vinculación y proyectos, 4) Comunicación.
    <br>
    <br>

    Cada uno de los ejes contienen elementos que sirven al mejoramiento del desempeño ambiental de la universidad,
    promueven la cultura de la
    sostenibilidad, coadyuvan a la generación del conocimiento ambiental y ayudan a una vinculación al interior y
    exterior universitario. Para
    lograr una comunicación efectiva, se crea el área de comunicación, que busca estrategias tanto al interior de la
    universidad como hacia el
    resto de la sociedad.
    <br>
    <br>
    Esta reorganización y fortalecimiento de algunas de las propuestas fundacionales deriva de las transiciones que de
    manera global hemos vivido
    y de las cuales hemos participado: desde las Metas del Milenio 2015 hasta la Agenda 2030 y los 17 Objetivos de
    Desarrollo Sostenible. Es por
    esto, que la Agenda Ambiental de la Universidad Autónoma de San Luis Potosí reafirma su compromiso con todas las
    entidades universitarias y
    con el resto de la sociedad para transitar a una sociedad más sostenible y con una cultura ambiental más
    fortalecida.
    <br>
    <br>
    Marcos Algara Siller
    <br>
    Director Agenda Ambiental.
