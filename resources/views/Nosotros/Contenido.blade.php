                            <h2 style="color:#616A7A;">¿QUIÉNES SOMOS?
                            </h2>
                            <br>
                            Desde 1998, la Universidad Autónoma de San Luis Potosí, cuenta con la Agenda
                            Ambiental, que es una instancia de coordinación transversal entre entidades
                            académicas y administrativas que busca impulsar, articular y apoyar iniciativas
                            que contribuyan a la incorporación de la perspectiva ambiental y de sostenibilidad
                            en todas las funciones sustantivas y de gestión no sólo hacia dentro de la
                            Universidad sino en toda la sociedad.
                            <br><br>
                            Con esta perspectiva responsable e integradora ha alcanzado sus objetivos a través
                            de proyectos académicos de calidad, evaluación continua y propuestas innovadoras
                            de investigación y tecnología.
                            <br><br>
                            Fomenta la capacitación, actualización y generación de conocimiento que contribuye
                            a mejorar el compromiso social con la formación de profesionistas altamente
                            especializados en temas de ambiente y sostenibilidad.
                            <br><br>
                            Para cumplir sus objetivos la Agenda Ambiental trabaja a través de 4 ejes:<br>
                            1. Educación e Investigación<br>
                            2. Gestión Institucional<br>
                            3. Vinculación y Proyectos<br>
                            4. Comunicación<br><br>
                            Con la comunicación e implementación de sus proyectos reafirma el compromiso
                            social de trabajar de manera activa, transversal e integradora con la comunidad
                            universitaria para fortalecer las acciones académicas, de investigación y
                            vinculación con la sociedad.<br><br>
                            La Universidad Autónoma de San Luis Potosí, consciente de la importancia del
                            cuidado del medio ambiente, ha establecido lineamientos que le permiten la
                            creación y transferencia del conocimiento en prácticas ambientales sostenibles,
                            fundamentadas con el Modelo Educativo y el
                            Plan Institucional de Desarrollo (PIDE UASLP 2013-2023) con el objetivo de
                            consolidarse como una Universidad Sustentable.
