            proteger el planeta y asegurar la prosperidad para todas las personas, 
            sin comprometer los recursos de las futuras generaciones. <br><br>
            El 25 de septiembre de 2015, la comunidad internacional se reunió en la Cumbre Mundial 
            sobre Desarrollo Sostenible de las Naciones Unidas, para crear la llamada Agenda 2030 
            para los siguientes 15 años.
            <br><br>
            Está conformada por 17 objetivos, conocidos como ODS, y 169 metas globales alineadas a 
            indicadores que deben ser medidos de manera sistemática, transparente y abierta.
            <br><br>
            En 2016 el Programa de las Naciones Unidas para el Desarrollo (PNUD) se acercó a la Escuela 
            de la Sociedad Digital de la Universidad de Ciencias Aplicadas de Ámsterdam, Holanda para 
            crear una colaboración que conectara a la comunidad de diseño con los ODS. Desde el nacimiento 
            de estos objetivos como reemplazo de los Objetivos de Desarrollo del Milenio, fueron conocidos 
            y utilizados dentro de los órganos políticos y gubernamentales, pero aún no estaban presentes 
            en las comunidades. El objetivo de los GGJ es justamente ese: empoderar a las personas locales para 
            crear un impacto para los ODS, resolviendo los desafíos locales “THINK BIG, START SMALL, ACT FAST”.
            <br><br>
            La Fundación Aguascalientes para la Cultura, la Academia y el Deporte, A.C. (FACULTAD, A.C.) es 
