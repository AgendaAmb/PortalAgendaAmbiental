            investigación científica con fines académicos, sociales, culturales y ambientales.
            <br><br>
            Está certificada por la Federación Internacional de la Juventud (IYF) de Londres, Inglaterra 
            con la finalidad de empoderar a la juventud en México, en congruencia con los Objetivos Mundiales 
            de la Agenda 2030.
            <br><br>
